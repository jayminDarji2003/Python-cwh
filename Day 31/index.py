# Sets in python
# Set is a collection of well defined objects.
"""
    Sets are --> 1. unordered
                 2. unindexed
                 4. not contained duplicates.
"""

# create set
s = {2,4,6,7,9}
print(s)

# print elements
for i in s:
    print(i)

# empty set
harry = set({})
print(type(harry))